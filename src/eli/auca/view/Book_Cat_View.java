    // Codes to populate Book category info into Table
    // Phone number: 0789037003
    private void populateBookCategory() {
        String[] colnames = {"CATEGORY ID", "CATEGORY NAME"};
        DefaultTableModel model = (DefaultTableModel) tableCategory.getModel();
        model.setRowCount(0);
        model.setColumnIdentifiers(colnames);
        BookCategoryDao cdao = new BookCategoryDao();
        List<Bookcategory> books = cdao.displayBookcategory();
        for (Bookcategory b : books) {
            Object[] obj = {b.getCategoryId(), b.getCategoryName()};
            model.addRow(obj);
        }
        tableCategory.setModel(model);
    }

    // Codes to add book category names into the combobox on Book form
    public void addCategoryToCombobox() {
        BookCategoryDao bDao = new BookCategoryDao();
        List<String> catNames = bDao.getCategoryNames();
        cmbBookCategory.removeAllItems();
        for (String catg : catNames) {
            cmbBookCategory.addItem(catg);
        }
    }

