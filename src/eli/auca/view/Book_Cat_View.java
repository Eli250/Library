    private void bookSaveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bookSaveBtnActionPerformed
        Book book = new Book();
        BookCategoryDao bkCatDao = new BookCategoryDao();

        book.setBookId(txtBookID.getText());
        book.setTitle(txtTitle.getText());
        book.setPublishingHouse(txtPublisher.getText());
        book.setDateOfPublication(dtPublication.getDate());
        book.setAuthor(txtAuthor.getText());
        book.setPages(Integer.parseInt(txtPages.getText()));
        book.setBookcategory(new Bookcategory(bkCatDao.getCategoryId(cmbBookCategory.getSelectedItem().toString())));
        
        BookDao bkDao = new BookDao();
        bkDao.insertBook(book);
        JOptionPane.showMessageDialog(this, "Book Inserted!");

        populateBook();

    }//GEN-LAST:event_bookSaveBtnActionPerformed

